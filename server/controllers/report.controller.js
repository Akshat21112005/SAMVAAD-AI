import PDFDocument from "pdfkit";
import InterviewSession from "../models/session.model.js";
import User from "../models/user.model.js";

function formatDate(d) {
  if (!d) return "";
  try {
    return new Date(d).toISOString().replace("T", " ").slice(0, 19);
  } catch {
    return String(d);
  }
}

export const getSessionReportPdf = async (req, res) => {
  try {
    const session = await InterviewSession.findOne({
      _id: req.params.sessionId,
      user: req.userId,
    }).lean();

    if (!session) {
      return res.status(404).json({ message: "Session not found" });
    }

    const user = await User.findById(req.userId).select("name email").lean();

    const doc = new PDFDocument({ margin: 50, size: "A4" });
    res.setHeader("Content-Type", "application/pdf");
    res.setHeader(
      "Content-Disposition",
      `attachment; filename="samvaad-report-${session._id}.pdf"`
    );
    doc.pipe(res);

    doc.fontSize(20).text("SAMVAAD-AI — Interview Report", { align: "center" });
    doc.moveDown();
    doc.fontSize(10).fillColor("#444444");
    doc.text(`Candidate: ${user?.name || "User"} · ${user?.email || ""}`, { align: "center" });
    doc.text(`Session type: ${session.type} · Difficulty: ${session.difficulty || "n/a"}`, {
      align: "center",
    });
    doc.text(`Completed: ${formatDate(session.completedAt || session.updatedAt)}`, {
      align: "center",
    });
    doc.moveDown(1.5);
    doc.fillColor("#000000");

    doc.fontSize(14).text("Overall score", { underline: true });
    doc.fontSize(12).text(`Total score: ${session.totalScore ?? 0} / 100`);
    doc.text(`Time taken (seconds): ${session.timeTaken ?? session.timeUsed ?? "n/a"}`);
    doc.moveDown();

    if (session.feedback) {
      doc.fontSize(14).text("Coaching summary", { underline: true });
      doc.fontSize(10).text(session.feedback, { align: "left" });
      doc.moveDown();
    }

    if (session.problem?.title) {
      doc.fontSize(14).text("DSA problem", { underline: true });
      doc.fontSize(10).text(`${session.problem.title}`, { continued: false });
      doc.moveDown(0.5);
    }

    const evaluations = session.evaluations
      ? session.evaluations instanceof Map
        ? [...session.evaluations.values()]
        : Object.values(session.evaluations)
      : [];
    if (evaluations.length) {
      doc.fontSize(14).text("Per-question scores", { underline: true });
      evaluations.forEach((ev, i) => {
        doc.fontSize(10).text(
          `Q${i + 1}: score ${ev.score ?? ev.percentageScore ?? "—"} — ${(ev.detailedFeedback || "").slice(0, 280)}`
        );
      });
      doc.moveDown();
    }

    doc.fontSize(8).fillColor("#888888").text("Generated by SAMVAAD-AI · samvaad report export", {
      align: "center",
    });

    doc.end();
  } catch (error) {
    return res.status(500).json({
      message: error.message || "PDF generation failed",
    });
  }
};
